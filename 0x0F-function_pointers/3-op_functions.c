#include "3-calc.h"

/**
 * op_add - adds integers
 * @a: first int
 * @b: second int
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts b from a
 * @a: first
 * @b: second
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies a with b
 * @a: first
 * @b: second
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide b from a
 * @a: numerator
 * @b: denominator
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a modulo b
 * @a: first
 * @b: second
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}

